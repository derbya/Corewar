#include "libft.h"

char		**ft_strsplits(char *s, char *c)
{
	int i;
	int	j;

	i = 0;
	while (c[i])
	{
		j = 0;
		while (s[j])
		{
			if (c[i] == s[j])
				s[j] = c[0];
			j++;
		}
		i++;
	}
	return (ft_strsplit(s, c[0]));
}
