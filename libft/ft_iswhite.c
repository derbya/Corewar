int	ft_iswhite(char c)
{
	if ((c == ' ') || (c == '\t'))
		return (1);
	if ((c == '\n') || (c == '\v'))
		return (1);
	if ((c == '\r') || (c == '\f'))
		return (1);
	return (0);
}
