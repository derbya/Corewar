#include "libft.h"

int	ft_strequi(char const *s1, char const *s2)
{
	if ((s1 == 0) && (s2 == 0))
		return (1);
	if ((s1 == 0) || (s2 == 0))
		return (0);
	if (ft_strcmpi(s1, s2) == 0)
		return (1);
	else
		return (0);
}
