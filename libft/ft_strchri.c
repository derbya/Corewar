int	ft_strchri(const void *s, int c)
{
	char	*str;
	int		i;

	str = (char *)s;
	i = 0;
	while (str[i] != (unsigned char)c)
	{
		if (str[i] == 0)
			return (-1);
		i++;
	}
	return (i);
}
